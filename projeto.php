                <h4>Introdução</h4>
                <p>Uma das ciências mais comentadas no dia a dia, a Meteorologia é uma área do conhecimento que envolve a análise de dados atmosféricos, geográficos, marítimos entre outros. Ela tenta compreender e prever os fenômenos climáticos que podem influenciar a agricultura ou até a escolha da uma roupa.<br>
                A aplicação web disponibiliza, por meio da Internet, informações meteorológicas de sensores instalados em uma região delimitada para coleta de dados. O usuário tem acesso a uma página contendo atualizações dos dados meteorológicos diários (temperatura máxima, temperatura mínima, luminosidade, umidade do solo e umidade do ar), bem como de geração de tabelas e pesquisas com resultados gráficos.</p>
                <h4>Metodologia</h4>
                <p>No desenvolvimento do projeto, foi observada a variação de temperatura e a incidência de chuvas na região e os prejuízos que as mudanças acarretavam para a agricultura familiar, levantada a questão foi idealizado a construção de uma estação meteorológica que permita o monitoramento remoto climático e ambiental, com o fim de obter dados diários sobre a temperatura, umidade do solo e ar e luminosidade.<br>
                Foi construído um sistema web utilizando HTML5, CSS3,PHP, servidor Apache® e o banco de dados Oracle® MySQL 5.2 . Foi criado um algoritmo em C para a plataforma Arduíno juntamente com os seus componentes de hardware.</p>
                <h4>Conclusão</h4>
                <p>Os resultados estão relacionados com o melhor entendimento dos fatores climáticos e construção de uma estação meteorológica, baseadas em hardware e software livres, que poderão servir como instrumentação científica para o estudo de fenômenos atmosféricos como também constituir-se em um recurso educacional e tecnológico.<br>
                Após coletados os dados referentes às condições climáticas de uma microrregião, depois de concluídas as analises foi emitidos resultados expressos em forma de gráficos os quais foram publicados em um site através de funções PHP e consultas ao banco de dados MySQL, para serem disponibilizados abertamente na internet, formando uma rede de registro e armazenamento de dados meteorológicos confiáveis em uma aplicação web de fácil acesso e entendimento.</p>
                <h4>Considerações Finais</h4>
                <p>Os dados coletados pela estação meteorológica desenvolvido na plataforma Arduíno forneceram resultados sobre as diversas características observadas, como luminosidade, temperatura, umidade do solo e umidade do ar, estabelecendo um perfil meteorológico sobre as variações climáticas na microrregião de Pedra Branca-CE, podendo ser acompanhada em tempo real por uma aplicação desenvolvida com tecnologias para a internet, facilitando assim a tomada de decisões referentes ao fator clima e alocação de recursos.</p>
